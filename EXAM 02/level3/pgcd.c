#include <stdio.h>

void	pgcd(int a, int b)
{
	int i = 1;
	int gcd = 0;
	while (i < a && i < b)
	{
		if (a % i == 0 && b % i == 0)
			gcd = i;
		i++;
	}
	printf("%d", gcd);
}
